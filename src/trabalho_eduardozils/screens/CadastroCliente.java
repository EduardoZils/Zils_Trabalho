    private Date formaData(int dia, int mes, int ano) {
        Date dataFormatada = null;
        try {
            if (dia < 32 && mes < 13 && ano <= 9999) {
                System.out.println("Data Valida! " + dia + "/" + mes + "/" + ano);

                dataFormatada = DateUtil.stringToDate(tfDIA.getText() + "/" + tfMES.getText() + "/" + tfANO.getText());
                System.out.println(tfDIA.getText() + "/" + tfMES.getText() + "/" + tfANO.getText());
                System.out.println(dataFormatada);
            }

        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(null, "Data Inválida, favor informar apenas numeros");
        }
        return dataFormatada;
    }
