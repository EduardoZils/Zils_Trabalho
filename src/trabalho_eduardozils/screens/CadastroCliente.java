    private void carregaTable(List<Cliente> clienteList) {
        if (clienteList == null) {
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();
        model.setRowCount(0);
        for (Cliente f : clienteList) {
            model.addRow(new Object[]{f.getCodigo(), f.getNome(), f.getCPF(), f.getDt_nasc()});
        }
    }

    private void Adicionar() throws SQLException {
        int dia = Integer.parseInt(tfDIA.getText());
        int mes = Integer.parseInt(tfMES.getText());
        int ano = Integer.parseInt(tfANO.getText());
        Date data = formaData(dia, mes, ano);
        System.out.println(data);
        Cliente f = new Cliente();
        f.setCodigo(clienteDAO.getLastId());
        f.setNome(tfNome.getText());
        f.setCPF(tfCPF.getText());
        f.setRG(tfRG.getText());
        f.setDt_nasc(data);
        clienteDAO.save(f);
        carregaTable(clienteDAO.getAll());
        limpaCampos();
    }

    private void Remover() {
        int codigoRemover = (int) tabela.getValueAt(tabela.getSelectedRow(), 0);
        try {
            clienteDAO.delete(codigoRemover);
            carregaTable(clienteDAO.getAll());
            JOptionPane.showMessageDialog(null, "Vendedor removido Com Sucesso!!!");
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        btRemover.setEnabled(false);
    }

    private void limpaCampos() {
        tfNome.setText("");
        tfCPF.setText("");
        tfRG.setText("");
        tfDIA.setText("");
        tfMES.setText("");
        tfANO.setText("");
    }
