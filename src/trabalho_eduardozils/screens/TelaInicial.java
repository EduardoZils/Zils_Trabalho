 private void carregaTable(List<Venda> listaVenda) {
        if (listaVenda == null) {
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();
        model.setRowCount(0);
        for (Venda f : listaVenda) {
            model.addRow(new Object[]{f.getCodigo(), f.getCodigo_carro(), f.getCodigo_cliente(), f.getValorTotal(), f.getCodigo_vendedor(), f.getData_venda()});
        }
    }

    private void attHora() {
        String data = "";
        data = DateUtil.dateToStringHora(new Date());
        jLabel4.setText(data);
    }

    private void filtrar() {
        try {
            if (rbCodigo.isSelected() && tfPesquisa.getText().trim().length() != 0) {
                Venda venda = vendaDAO.getById(Integer.parseInt(tfPesquisa.getText()));
                List<Venda> vendaList = new ArrayList<>();
                vendaList.add(venda);
                carregaTable(vendaList);
            } else if (rbValor.isSelected() && tfPesquisa.getText().trim().length() != 0) {
                carregaTable(vendaDAO.getByName(tfPesquisa.getText()));
            } else {
                carregaTable(vendaDAO.getAll());
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

